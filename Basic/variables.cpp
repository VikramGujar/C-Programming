// This program demonstrates the declaration and initialization of variables with different data types.


#include <iostream>

int main() {
    int age = 25;             
    float height = 5.9;        
    char initial = 'V';       
    bool isStudent = true;     
    double pi = 3.14159;       

    std::cout << "Age: " << age << std::endl;
    std::cout << "Height: " << height << std::endl;
    std::cout << "Initial: " << initial << std::endl;
    std::cout << "Is student: " << isStudent << std::endl;
    std::cout << "Pi: " << pi << std::endl;

    return 0;
}
